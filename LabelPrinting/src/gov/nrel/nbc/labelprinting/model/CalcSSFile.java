package gov.nrel.nbc.labelprinting.model;

/**
 * This is a data transfer object class for <CalcSSFile>.
 * A <CalcSSFile> represents the source spreadsheet.
 *
 * @author jalbersh
 */
public class CalcSSFile {
	
	/**
	 * Generated unique identifier with no business meaning
	 */
	private long id;
	
	/**
	 * File Name
	 */
	private String name;
	
	/**
	 * File Path
	 */
	private String path;
	
	/**
	 * Simple constructor
	 */
	public CalcSSFile() {
		
	}

	/**
	 * @param name - original name of file
	 * @param path - current path of saved file on server
	 */
	public CalcSSFile(String name, String path) {
		this.name = name;
		this.path = path;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}

	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param path the path to set
	 */
	public void setPath(String path) {
		this.path = path;
	}

	/**
	 * @return the path
	 */
	public String getPath() {
		return path;
	}
}
