package gov.nrel.nbc.labelprinting.model;

/**
 * This is a data transfer object class. A <Fractions> represents a
 * fraction for a sample.
 * 
 * @author jalbersh
 */
public class Fractions {
	
	/**
	 * Generated unique identifier with no business meaning
	 */
	private long id;
	
	/**
	 * Display name.
	 */
	private String name;
	
	/**
	 * Simple constructor
	 */
	public Fractions() {
		name = null;
	}
	
	/**
	 * Complex constructor
	 * 
	 * @param value
	 */
	public Fractions(String value) {
		name = value;
	}
	
	public long getId() {
		return id;
	}
	
	public void setId(long id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
}
